/**
* @module isdefined
*/

'use strict';

/**
 * Return `true` if and only if *value* is `undefined`, otherwise return `false`.
 * 
 * @func isundefined
 * @param {any} value The value to check
 * @returns {boolean}
 */
module.exports = function isundefined(value) {
    return (value === undefined);
}