/**
 * @module arrays/length
 */

'use strict';

/**
 * Return the value of *array*'s `length` property.
 * 
 * @function length
 * @param {object} array The object to retrieve the `length` property from
 * @returns {number}
 */
function length(array) {
    return array.length;
}

module.exports = length;